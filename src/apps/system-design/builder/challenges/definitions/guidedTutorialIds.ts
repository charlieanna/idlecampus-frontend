// Check if a challenge ID has a guided tutorial
export function hasGuidedTutorial(challengeId: string): boolean {
  const normalizedId = challengeId.replace(/_/g, '-').toLowerCase();
  return GUIDED_TUTORIAL_IDS.has(normalizedId);
}

// Get total count
export function getGuidedTutorialCount(): number {
  return GUIDED_TUTORIAL_IDS.size;
}
